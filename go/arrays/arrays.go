package main // arrays.go

// Arrays Test

import "fmt"

//-------------------------------------------------------------------------------

func main() {

	var arr []byte

	arr = make([]byte, 3)
	arr[0] = 0x00
	arr[1] = 0x01
	arr[2] = 0x02
	fmt.Println(arr) //

	f(arr)
	fmt.Println(arr) //

	g(&arr)
	fmt.Println(arr) //

	h(arr)
	fmt.Println(arr) //

	i(&arr)
	fmt.Println(arr) //
}

//-------------------------------------------------------------------------------

func f(array []byte) {

	array[1] = 0xFF
}

//-------------------------------------------------------------------------------

func g(array *[]byte) {

	(*array)[1] = 0x11
}

//-------------------------------------------------------------------------------

func h(a []byte) {

	a = make([]byte, 3)
	a[0] = 0x03
	a[1] = 0x04
	a[2] = 0x05
}

//-------------------------------------------------------------------------------

func i(a *[]byte) {

	*a = make([]byte, 3)
	(*a)[0] = 0x06
	(*a)[1] = 0x07
	(*a)[2] = 0x08
}

